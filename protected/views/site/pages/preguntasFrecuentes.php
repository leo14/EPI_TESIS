				a la Etapa de Desarrollo de Proyectos de Innovación. Un alumno, durante el primer
				semestre, podría participar del Programa en su Etapa de Formación y luego en el
				segundo semestre postular a la Etapa de Desarrollo de Proyectos de Innovación, en ese
				caso al momento de postular su tesis innovadora podrá solicitar la convalidación de la
				asignatura de formación integral.

