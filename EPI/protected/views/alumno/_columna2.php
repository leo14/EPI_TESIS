
<h1 class="titulo" > Detalle de la inscripción</h1>


<p style="text-align: justify;font-size: 15px;color: #928e8e;" id="detalleInscripcion">
Pueden inscribirse alumnos de cualquier carrera de la Universidad del Bío-Bío, campus Concepción y Chillán, que estén habilitados para inscribir actividad de titulación, o bien, estén realizando su seminario 1, en el año académico 2015. (Si tienes dudas puedes consultar en <a href="mailto:epi@ubiobio.cl">epi@ubiobio.cl</a>).
<br>
<br>
Esta inscripción habilita para ingresar a la primera etapa: Etapa de Formación. Esta etapa consiste en un entrenamiento intensivo en conceptualización y modelos de innovación, además de recibir un entrenamiento en metodologías para generar la innovación. Esta capacitación se puede convalidar con una asignatura de formación integral (oferta institucional).
<br>
<br>
El curso tiene cupos limitados, se enviará un correo para confirmar que ha sido aceptada o rechazada la inscripción y puede participar del programa, en el mismo correo se entregarán las instrucciones para ingresar a la plataforma del curso.
<br>
<br>
Puedes revisar las bases de la convocatoria para ver las fechas y horarios de realización de las clases y talleres. Accede en este <a href="<?php echo Yii::app()->request->baseUrl; ?>/Bases.pdf" target="_blank">link</a>.
<br>
<br>
La Etapa de Formación es requisito para postular a la segunda etapa, Etapa de Desarrollo de Proyectos de Innovación, que les permitirá optar por el financiamiento de $250.000.
</p>










